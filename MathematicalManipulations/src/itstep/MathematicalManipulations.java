package itstep;

public class MathematicalManipulations {

	public int getAmount(int n1, int n2) {

		return n1 + n2;
	}
	
	public int getMultiplication(int n1, int n2) {
		
		return n1 * n2;
	}
	
	public int getDivision( int n1, int n2 ) {
		
		return n1 / n2;
	}
	
	public int getDerogation( int n1, int n2) {
		
		return n1 - n2;
	}


}
